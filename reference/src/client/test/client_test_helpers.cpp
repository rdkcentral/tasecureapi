    std::string iso8601(const uint64_t instant) {
        static const char* ISO_TIME_FORMAT = "%Y-%m-%dT%H:%M:%SZ";

        auto time = static_cast<time_t>(instant);
        struct tm ts {};
        gmtime_r(&time, &ts);

        char buf[256];
        strftime(buf, sizeof(buf), ISO_TIME_FORMAT, &ts);

        return {buf};
    }

